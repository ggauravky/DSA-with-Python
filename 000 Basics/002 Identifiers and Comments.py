#Identifiers:
# An identifier is the name used to identify a variable, function, class, module, or other object.
# An identifier must start with a letter (A-Z, a-z) or an underscore (_) followed by letters, digits (0-9), or underscores.
# Identifiers are case-sensitive and cannot be the same as Python keywords.
my_variable = 42
def my_function():
    return "Hello, World!"
print(my_variable)
print(my_function())

#Comments:
# A comment is a piece of text in a program that is not executed.
# Comments are used to explain code and make it more understandable.
# In Python, comments start with the hash character (#) and extend to the end of the line.
# This is a single-line comment explaining the next line of code.   
print("This line of code will be executed.")
# This is another comment.
# Comments can also be used to temporarily disable code.    
