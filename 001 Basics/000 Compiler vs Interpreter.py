# Compiler vs. Interpreter


# | Feature               | Compiler                              | Interpreter                                              |
# |-----------------------|--------------------------------------|--------------------------------------------------------   |
# | Translation Time      | Translates entire code before execution | Translates code line-by-line during execution          |
# | Execution Speed       | Generally faster execution after compilation | Generally slower due to line-by-line translation
# | Error Detection       | Detects errors before execution      | Detects errors during execution                          |
# | Output                | Produces an independent executable file | No separate executable file; runs code directly       |
# | Examples              | C, C++, Java                         | Python, JavaScript, Ruby                                 |


# Note: Python is primarily an interpreted language, meaning it translates and executes code line-by-line. However, it also compiles code to bytecode before execution, which is then interpreted by the Python Virtual Machine (PVM). This hybrid approach allows Python to benefit from both compilation and interpretation.